func GetIcon(c byte) string {
	if c >= 'A' && c <= 'Z' {
		return strings.Join(Icons[c-'A'], "\n")
	}
	return ""
}

func IconPrint(s string) {
	n := len(Icons[0])
	for row := 0; row < n; row++ {
		line := ""
		for _, c := range []byte(s) {
			if c >= 'A' && c <= 'Z' {
				line += Icons[c-'A'][row] + "  "
			}
		}
		fmt.Println(line)
	}
}
